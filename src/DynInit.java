/**
 * Динамическая инициализация.
 */


public class DynInit {
	public static void main(String[] args) {
		double a = 3.0, b = 4.0;
		
		System.out.println("Катет a = " + a);
		System.out.println("Катет b = " + b);
		double c = Math.sqrt(a * a + b * b);
		System.out.println("Гипатинуза c = " + c);
	}
}
