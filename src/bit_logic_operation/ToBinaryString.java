package bit_logic_operation;

/**
 * Вывод числа на консоль в бинарном виде
 */
public class ToBinaryString {
	public static void main(String[] args) {
		int x = -2;
		System.out.println("Integer.toBinaryString(1) = " + Integer.toBinaryString(0));
		System.out.println("Integer.toBinaryString(1) = " + Integer.toBinaryString(1));
		System.out.println("Integer.toBinaryString(2) = " + Integer.toBinaryString(2));
		System.out.println("Integer.toBinaryString(x) = " + Integer.toBinaryString(x));
		
	}
	
}
