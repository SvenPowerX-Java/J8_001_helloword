public class Example2 {
	public static void main(String[] args) {
		int num;    // вэтой строке кода объявляется переменная с именем num
		num = 100;  //переменной num присваивается значение 100
		System.out.println("Это переменная num:");
		System.out.println("num = " + num);
		num = num * 2;
		System.out.println("Значение переменной num * 2:");
		System.out.println("num = " + num);
	}
}
